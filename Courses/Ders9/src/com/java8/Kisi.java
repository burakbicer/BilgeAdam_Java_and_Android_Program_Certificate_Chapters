package com.java8;

public class Kisi {

	private String ad;
	private Integer maas;
	private Integer yas;

	public Kisi(String ad, Integer maas, Integer yas) {
		super();
		this.ad = ad;
		this.maas = maas;
		this.yas = yas;
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public Integer getMaas() {
		return maas;
	}

	public void setMaas(Integer maas) {
		this.maas = maas;
	}

	public Integer getYas() {
		return yas;
	}

	public void setYas(Integer yas) {
		this.yas = yas;
	}

}
