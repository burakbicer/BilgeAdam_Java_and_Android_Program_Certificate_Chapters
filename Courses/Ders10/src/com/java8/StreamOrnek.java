package com.java8;

//POJO: Plain Old Java Object
public class StreamOrnek {

	public class ornekPojo {

		private Long id;
		private String ad;
		private String soyad;
		private String tc;

		public Long getId() {
			return id;
		}

		public void setId(Long id) {
			this.id = id;
		}

		public String getAd() {
			return ad;
		}

		public void setAd(String ad) {
			this.ad = ad;
		}

		public String getSoyad() {
			return soyad;
		}

		public void setSoyad(String soyad) {
			this.soyad = soyad;
		}

		public String getTc() {
			return tc;
		}

		public void setTc(String tc) {
			this.tc = tc;
		}

	}
}
