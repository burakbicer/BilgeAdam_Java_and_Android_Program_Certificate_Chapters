package com.corecollection;

public class Kisi {

	private String ad;
	private String soyad;
	private String tc;

	public Kisi(String ad, String soyad, String tc) {
		super();
		this.ad = ad;
		this.soyad = soyad;
		this.tc = tc;
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public String getSoyad() {
		return soyad;
	}

	public void setSoyad(String soyad) {
		this.soyad = soyad;
	}

	public String getTc() {
		return tc;
	}

	public void setTc(String tc) {
		this.tc = tc;
	}

}
